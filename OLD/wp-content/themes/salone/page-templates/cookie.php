Alcuni dei cookie di terza parte potrebbero anche essere impiegati per finalità di profilazione dell’utente e per raccogliere, dunque, informazioni concernenti le preferenze manifestate dall’utente stesso durante la propria navigazione Internet. Per un maggiore dettaglio circa i cookie di terze parti impiegati anche per finalità di profilazione, si veda la tabella sottostante.
