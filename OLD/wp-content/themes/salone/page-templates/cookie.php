		<li>Advertsing: rientrano in questa categoria i cookie utilizzati per erogare servizi pubblicitari all’interno di un determinato sito web;</li>

		<li>Widgets: alcuni widget messi a disposizione dai social network (come, ad esempio, i pulsanti di Facebook, Twitter, Pinterest, etc.) possono utilizzare propri cookie per facilitare l’interazione con il social network di riferimento, consentendo all’utente di condividere i contenuti di una determinata pagina del nostro sito web. La disattivazione di tali cookie di terze parti non compromette l’utilizzo del sito, se non con unico riferimento alla possibilità di condivisione rapida dei contenuti.</li>
	</ul>
</p>
