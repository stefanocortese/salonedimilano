	    	<p>Per mezzo della presente informativa sui cookies, redatta conformemente al provvedimento del Garante per la protezione dei dati personali (di seguito, il “Garante”) dell’8 maggio 2014, n. 229, recante “Individuazione delle modalità semplificate per l’informativa e l’acquisizione del consenso per l’uso dei cookie” (di seguito, il “Provvedimento Cookie”) Beauty & Business S.p.A., con sede legale in  via Ciserano, 24046, Osio Sotto (BG), Italia (di seguito, “B&B”), in qualità di titolare del trattamento, intende illustrare le tipologie di cookie che vengono utilizzati dal sito web www.ilsalonemilano.com/it/, nonché le relative finalità e modalità di utilizzo e di gestione.
La presente informativa su cookie costituisce parte integrante dell’informativa sul trattamento dei dati personali, consultabile al link in calce a ciascuna pagina del nostro sito web.
</p>
<p>
<h5>Cosa sono i cookie?</h5>
Da un punto di vista generale, i cookie sono stringhe di testo di piccole dimensioni che i siti web visitati dall’utente inviano al suo terminale (solitamente al browser), dove vengono memorizzati per essere poi ritrasmessi agli stessi siti web alla successiva visita del medesimo utente.
Nel corso della navigazione su un determinato sito web, l’utente può ricevere sul suo terminale sia i cookie del medesimo sito web visitato, sia cookie che vengono invece inviati da siti web o da web server diversi (vale a dire cookie di “terze parti”), sui quali possono risiedere alcuni elementi (come, ad esempio, immagini, mappe, suoni, specifici link a pagine di altri domini, etc.) che vengono mostrati sul sito web che lo stesso utente sta visitando.
I cookie possono generalmente essere usati per differenti finalità come, ad esempio, per l’esecuzione di autenticazioni informatiche, per il monitoraggio di sessioni, per la memorizzazione di informazioni su specifiche configurazioni riguardanti gli utenti che accedono al server, per finalità di profilazione, etc.
</p>
<p>
<h5>Cookie di navigazione o di sessione</h5>
I cookie di “navigazione” o di “sessione” sono fondamentali per permettere all’utente di muoversi all’interno del nostro sito internet, per svolgere le attività strettamente necessarie al funzionamento del sito stesso e, dunque, per garantirne la normale navigazione e fruizione. Questi cookie non vengono memorizzati in modo persistente sul computer dell’utente e, conseguentemente, svaniscono con la chiusura del browser.
L’utilizzo di tali cookie e lo svolgimento dei trattamenti ad essi connessi, non richiede, ai sensi della disciplina vigente, il consenso preventivo dell’utente. Tuttavia, l’utente può scegliere di negare il consenso all’installazione di tali cookie per il tramite delle impostazioni del proprio browser, consapevole del fatto che una simile scelta potrebbe influenzare negativamente la propria permanenza sul nostro sito web.
</p>

<p>
<h5>Cookie di funzionalità</h5>
I cookie di “funzionalità” – che, ai sensi del Provvedimento Cookie, rientrano nella tipologia dei c.d. cookie tecnici – permettono al nostro sito web di ricordare alcune scelte dell’utente (come, ad esempio, la comunicazione della sua posizione geografica e le preferenze concernenti la lingua), al fine di fornire a quest’ultimo una navigazione maggiormente personalizzata ed ottimizzata (come, ad esempio, la ricerca e l’individuazione del centro più vicino alla posizione geografica comunicata dall’utente e la descrizione dei contenuti del nostro sito web nella lingua scelta dall’utente).
Si precisa che questi cookie non vengono impiegati da B&B per finalità di profilazione della navigazione web dell’utente, ma permettono unicamente la personalizzazione del servizio reso da B&B. Le informazioni raccolte per il tramite di questi cookie sono anonime.
I cookie di funzionalità non sono indispensabili al funzionamento del nostro sito web, ma ne migliorano la qualità e l’esperienza di navigazione dell’utente. Conseguentemente, se non si accettano tali cookie, la resa e la funzionalità di www.ilsalonemilano.com/it/ potrebbero risultare inferiori e l’accesso a determinati contenuti del sito potrebbe essere limitato.
L’utilizzo di questi cookie e lo svolgimento dei trattamenti ad essi connessi non richiedono, ai sensi della disciplina vigente, il consenso preventivo dell’utente. Tuttavia, l’utente può scegliere di negare il consenso all’installazione di tali cookie per il tramite delle impostazioni del proprio browser.
</p>

<p>
<h5>Cookie di terze parti</h5>
Quando un utente naviga sul nostro sito web è possibile che vengano memorizzati alcuni cookie che non sono controllati e gestiti da B&B. Questo accade, per esempio, se l’utente visita una pagina che include i contenuti di un sito web di terzi. Di conseguenza, l’utente riceverà cookie da questi servizi di terze parti. Sui cookie di terza parte B&B non ha il controllo delle informazioni fornite dal cookie e non ha accesso a tali dati. Queste informazioni sono controllate totalmente dalle società terze secondo quanto descritto nelle rispettive privacy policy e cookie policy. 
</p>

